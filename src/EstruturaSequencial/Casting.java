package EstruturaSequencial;

public class Casting {
    //Casting eh uma conversao explicita do tipo de dados
    public static void main(String[] args) {

        double a = 5;
        int b = (int) a; // Fazendo o casting de double para int

        System.out.println(b);

        int x, y;
        x = 6;
        y = 4;
        double resultado;
        resultado = x + y;

        System.out.println(resultado);
    }
}
